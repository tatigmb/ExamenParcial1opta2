    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
    int id_cliente = Integer.parseInt(idcliente.getText().trim());
    int id_persona = Integer.parseInt(idpersona.getText().trim());
    String fecha_ingreso = fecha.getText();
    String calificacion = cali.getText();
    String estado = estad.getText();
   
    
    ClienteModel clienteActualizar = new ClienteModel();
    
    String txtidcli = idcliente.getText();
    int idclientes = Integer.parseInt(txtidcli);
    clienteActualizar.setId_cliente(idclientes);
    
    String txtidper = idpersona.getText();
    int idpersonas = Integer.parseInt(txtidper);
    clienteActualizar.setId_persona(idpersonas);
    
    clienteActualizar.setFecha_ingreso(fecha.getText());
    clienteActualizar.setCalificacion(cali.getText());
    clienteActualizar.setEstado(estad.getText());
    cliente1.modificarClientes(clienteActualizar);
    
    idcliente.setText("");
    idpersona.setText("");
    fecha.setText("");
    estad.setText("");
    cali.setText("");
    idcliente.setEditable(true);
    }//GEN-LAST:event_modificarActionPerformed

